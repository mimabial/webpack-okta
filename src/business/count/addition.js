/**
 * Business function example. Adds up a value to a current value.
 * @param {number} currentValue The current value
 * @param {number} valueToAdd The value to add to the current value
 * @returns {number} The sum of values. No input is modified.
 */
function addition(currentValue, valueToAdd) {
  return currentValue + valueToAdd;
}

export default addition;
